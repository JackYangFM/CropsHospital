using System.Web.Optimization;

namespace Hospital.Terminal
{
    public class BundleConfig
    {
        // 有关绑定的详细信息，请访问 http://go.microsoft.com/fwlink/?LinkId=301862
        public static void RegisterBundles(BundleCollection bundles)
        {
            #region 基础样式

            const string baseCss1 = "";
            #endregion

            #region 基础JS
            const string baseJs1 = "";
            #endregion

            #region 通用

            bundles.Add(new StyleBundle("~/cms/css/Common").Include(baseCss1));
            bundles.Add(new ScriptBundle("~/cms/js/Common").Include(baseJs1));

            #endregion

        }
    }
}